
const DEFAULT_PORT = 3001;

function normalizePort(val) {
    let port = parseInt(val, 10);

    if (isNaN(port)) {
        // named pipe
        return val;
    }

    if (port >= 0) {
        // port number
        return port;
    }

    return false;
}

module.exports = normalizePort(process.env.PORT || DEFAULT_PORT);